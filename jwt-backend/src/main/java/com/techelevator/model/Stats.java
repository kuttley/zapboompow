package com.techelevator.model;

public class Stats {
	
	private int collectionsCount;
	private int publicCollectionsCount;
	private int comicsCount;
	private int usersCount;
	private int premiumUsersCount;

	public int getCollectionsCount() {
		return collectionsCount;
	}
	public void setCollectionsCount(int collectionsCount) {
		this.collectionsCount = collectionsCount;
	}
	public int getPublicCollectionsCount() {
		return publicCollectionsCount;
	}
	public void setPublicCollectionsCount(int publicCollectionsCount) {
		this.publicCollectionsCount = publicCollectionsCount;
	}
	public int getComicsCount() {
		return comicsCount;
	}
	public void setComicsCount(int comicsCount) {
		this.comicsCount = comicsCount;
	}
	public int getUsersCount() {
		return usersCount;
	}
	public void setUsersCount(int usersCount) {
		this.usersCount = usersCount;
	}
	public int getPremiumUsersCount() {
		return premiumUsersCount;
	}
	public void setPremiumUsersCount(int premiumUsersCount) {
		this.premiumUsersCount = premiumUsersCount;
	}
}
